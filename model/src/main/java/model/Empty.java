package model;

/**
 * <h1>The Class Empty.</h1>
 *
 * @author Alyssa
 * @version 1.0
 */
public class Empty {
	/** The x. */
	private int x;
	/** The y. */
	private int y;

	/**
	 * Construtor Empty Initialize the x and y.
	 * 
	 * @param x
	 * 			the x
	 * @param y
	 * 			the y
	 */
	public Empty(int x, int y) {
		this.x = x;
		this.y = y;
	}

	/**
	 * Get X.
	 * 
	 * @return the x
	 */
	public int getX() {
		return x;
	}

	/**
	 * Set X.
	 * 
	 * @param x
	 *            the x to set
	 */
	public void setX(int x) {
		this.x = x;
	}

	/**
	 * Get Y.
	 * 
	 * @return the y
	 */
	public int getY() {
		return y;
	}

	/**
	 * Set Y.
	 * 
	 * @param y
	 *            the y to set
	 */
	public void setY(int y) {
		this.y = y;
	}
}